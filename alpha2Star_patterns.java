
//printing the star patterns
import java.util.*;

public class alpha2Star_patterns {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println("enter n:");
        int n = sc.nextInt();

        for(int i=0;i<n;i++){
            if(i==1||i==2){
                System.out.print("*   *");
            }
            System.out.println();
            
            for(int j=0;j<6;j++){
                System.out.print("*");
            }
            
             }
        }
        }
    
